;***************************************************************
;	AKI-80ゴールドボード単体
;***************************************************************

;*******************************
;	定数定義
;*******************************
RAM_B	EQU	8000H

;***************************************************************
;	メインルーチン
;***************************************************************
	ORG	RAM_B

START:
	LD	HL, 0000H
	LD	A, (PARAM2)
	CP	00H
	JR	Z, MUL_E
	LD	B, A
	LD	A, (PARAM1)
	LD	D, 00H
	LD	E, A
LOOP:
	ADD	HL, DE
	DJNZ	LOOP
MUL_E;
	LD	(RESLT1), HL
	NOP
	JR	START

;*******************************
;	ROM領域
;*******************************
	ORG	RAM_B + 40H

PARAM1:	DEFB	0AAH
PARAM2:	DEFB	55H

;*******************************
;	RAM領域
;*******************************
	ORG	RAM_B + 80H

RESLT1:	DEFS	2

	END
